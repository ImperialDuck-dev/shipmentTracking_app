    private void show_shipmentDetails() {
        try{
            
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/shipmenttracking?zeroDateTimeBehavior=CONVERT_TO_NULL","root","142smb");
           Statement st = con.createStatement();

           String query = "SELECT trade_update_date, run_date, estimated_arrival_date,foreign_port_lading,place_of_receipt,port_of_destination,actual_arrival_date,consignee_name,shipper_name "
                   + "     FROM shipment_details AS S, consignee_table AS C, shipper_table as SH "
                   + "     WHERE S.Consignee_ID=C.consignee_ID AND S.Shipper_ID=SH.shipper_ID AND shipment_ID = "+SearchID.getText()+" ";

            ResultSet rs = st.executeQuery(query);
        
           if(rs.next()){
               jPanel8.setVisible(true);
               main_panel.show(jPanel8, "shipment_card");
               
               txt_TUD.setText(rs.getString(1));
               txt_RD.setText(rs.getString(2));
               txt_EAD.setText(rs.getString(3));
               txt_FPL.setText(rs.getString(4));
               txt_POR.setText(rs.getString(5));
               txt_POD.setText(rs.getString(6));
               txt_items.setText(rs.getString(7));
               txt_cname.setText(rs.getString(8));
               txt_sname.setText(rs.getString(9));
               
           }   
           
           else jPanel8.setVisible(false);
           
           st.close();
           con.close();
           
           
       } catch (Exception e){
             e.printStackTrace();
             
       }
    }

    private void show_cargoDetails() {
    try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/shipmenttracking?zeroDateTimeBehavior=CONVERT_TO_NULL","root","142smb");
           Statement st = con.createStatement();
           String query = "SELECT C.*,Vessel_Name FROM cargo C, container_number CN WHERE shipment_ID="+SearchID.getText()+" AND C.container_number=CN.Container_ID";
          
           ResultSet rs = st.executeQuery(query);
           ResultSetMetaData rsmd = rs.getMetaData();
           DefaultTableModel model = (DefaultTableModel) tbl_cargo.getModel();
           model.setRowCount(0);
         
           
           String serial_number, description_text, piece_count,shipment_ID, container_number,Vessel_Name;
           while(rs.next()){
               serial_number = rs.getString(1);
               description_text = rs.getString(2);
               piece_count = rs.getString(3);
               shipment_ID = rs.getString(4);
               container_number = rs.getString(5);
               Vessel_Name = rs.getString(6);
               String[] row = {serial_number, description_text, piece_count,shipment_ID, container_number,Vessel_Name};
               model.addRow(row);
           }   
           
           st.close();
           con.close();
           
           
       } catch (Exception e){
             e.printStackTrace();
             
       }
    }
    
    
